#include <stdio.h>
/**
  * op_add - add two numbers
  * @a: first integer
  * @b: second integer
  * Return: sum
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - subtraction
  * @a: first number
  * @b: second number
  * Return: result
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multibly two numbers
  * @a: first number
  * @b: second number
  * Return: product
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - divide two numbers
  * @a: first number
  * @b: second number
  * Return: result
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - modulus
  * @a: first number
  * @b: second number
  * Return: result
  */
int op_mod(int a, int b)
{
	return(a % b);
}
